    <h1>Regulamin Eventu</h1>
    <p><div class="points">1. Przedmiot Regulaminu</div> <br>
a. Niniejszy regulamin (Regulamin) określa warunki uczestnictwa w wydarzeniu (Event).
Jeżeli warunki uczestnictwa zostaną określone także w innych dokumentach np. w opisie
Eventu, regulaminie obiektu, formularzu zgłoszeniowym lub zleceniu usług związanych z
Eventem, Regulamin uzupełni te dokumenty i znajdzie zastosowanie w zakresie w nich
nieuregulowanym. W przypadku gdy określone uczestnictwo w Evencie oraz usługi z nim
związane są odpłatne ww. dokumenty opisują warunki wykonania i rozliczenia
wzajemnych świadczeń. <br><br>
b. Organizatorem wydarzenia jest podmiot wskazany w opisie Eventu lub formularzu
zgłoszeniowym (Organizator).<br><br>
c. Regulamin ma zastosowanie do Eventów typu kongres, konferencja, zjazd, turniej,
spotkanie, sympozjum, konwencja, forum, szkolenie, wystawa, targi, bal, bankiet,
koncert lub inne wydarzenie sportowe, kulturalne lub artystyczne - dla których
Organizator zastrzegł akceptację niniejszego Regulaminu lub udostępnił go w związku z
danym Eventem w sposób umożliwiający przechowywanie i odtwarzanie w zwykłym toku
czynności. W razie wątpliwości akceptację Regulaminu wyraża się także poprzez sam
udział w Evencie lub wpłatę wynagrodzenia za uczestnictwo w wydarzeniu lub usługi z
nim związane.<br><br>
d. Jeżeli z Regulaminu nie wynika nic innego, wiąże on wszystkich uczestników Eventu tj.
osoby, które dokonują rejestracji na Event, w tym w cudzym imieniu lub jako
przedstawiciele instytucji lub firmy lub które faktycznie uczestniczą w Evencie w
charakterze odwiedzających, widzów, gości, wystawców, partnerów, sponsorów,
moderatorów, prelegentów lub ich przedstawicieli (Uczestnik). W razie wątpliwości za
Uczestnika uznaje się każdą osobę, która wyraziła wolę wejścia na teren obiektu, w
którym odbywa się Event i która została na ten teren wpuszczona.<br><br>
e. Uczestnikiem może być wyłącznie osoba pełnoletnia, chyba że Organizator udzieli
indywidualnej zgody na uczestnictwo także innym osobom. W przypadku umożliwienia
uczestnictwa w Evencie osobie poniżej 13 roku życia, za przestrzeganie przez nią
warunków Regulaminu odpowiada jej prawny opiekun.<br><br>
f. W przypadku nabycia prawa do udziału w Evencie za pośrednictwem innych podmiotów,
w tym serwisów zajmujących się dystrybucją biletów, Uczestnika wiążą także warunki
nabycia wskazane przez te podmioty.<br><br>
<div class="points">2. Rejestracja i uczestnictwo w Evencie</div><br>
a. Warunkiem uczestnictwa w Evencie jest rejestracja danej osoby przez formularz
zgłoszeniowy przekazany Organizatorowi za pośrednictwem systemu rejestracyjnego online 
lub w inny sposób określony przez Organizatora. W przypadku Eventów lub
uczestnictwa odpłatnego, warunkiem uczestnictwa lub uzyskania dodatkowych usług z
nim związanych będzie - zamiast lub obok rejestracji – zamówienie, wniesienie
odpowiedniej opłaty lub okazanie biletu wstępu.<br><br>
b. Organizator określa w formularzu zgłoszeniowym, zamówieniu lub w inny sposób,
wymagania i dane Uczestnika konieczne do rejestracji na dany Event, nabycia prawa
uczestnictwa w Evencie lub do skorzystania z określonych usług. Jeżeli uzasadnia to
charakter Eventu, nieodpłatność udziału lub ograniczenia związane z obiektem,
Organizator może odmówić rejestracji lub uzależnić rejestrację danej osoby od
dodatkowej weryfikacji, udzielenia dodatkowej zgody na wykorzystanie danych lub od
określonego terminu zgłoszenia.
</p>

